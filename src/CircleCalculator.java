public class CircleCalculator {
    // create main class
    public static void main(String[] args) {
        // declare variables
        double circleRadius = 12;
        double circleDiameter = circleRadius * 2;
        double circleCircumference = circleRadius * 2 * 3.14;
        double circleArea = circleRadius * circleRadius * 3.14;
        // output results
        System.out.println("Diameter: " + circleDiameter + "cm");
        System.out.println("Circumference: " + circleCircumference + "cm");
        System.out.println("Area: " + circleArea + "cm^2");

    }
}
